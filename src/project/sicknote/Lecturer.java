package project.sicknote;

/**
 *
 * @author 209215178
 */
public class Lecturer {
    
    private int lecturerID;
    private String lecturerName;
    private String lecturerSurname;
    private String lecturerDOB;
    private String lecturerDOR;
    
    public Lecturer(){}
    

    public int getLecturerID() {
        return lecturerID;
    }

    public void setLecturerID(int lecturerID) {
        this.lecturerID = lecturerID;
    }

    public String getLecturerName() {
        return lecturerName;
    }

    public void setLecturerName(String lecturerName) {
        this.lecturerName = lecturerName;
    }

    public String getLecturerSurname() {
        return lecturerSurname;
    }

    public void setLecturerSurname(String lecturerSurname) {
        this.lecturerSurname = lecturerSurname;
    }

    public String getLecturerDOB() {
        return lecturerDOB;
    }

    public void setLecturerDOB(String lecturerDOB) {
        this.lecturerDOB = lecturerDOB;
    }

    public String getLecturerDOR() {
        return lecturerDOR;
    }

    public void setLecturerDOR(String lecturerDOR) {
        this.lecturerDOR = lecturerDOR;
    }
    
    
}
